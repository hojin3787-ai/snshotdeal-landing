export default function BlogPost({
  params,
}: {
  params: Promise<{ slug: string }>;
}) {
  const { slug } = use(params);
  const post = blogPosts[slug as keyof typeof blogPosts];
  const [copiedLink, setCopiedLink] = useState(false);

  if (!post) {
    return (
      <main className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <h1 className="text-4xl font-bold text-gray-900 mb-4">
            포스트를 찾을 수 없습니다
          </h1>
          <Link
            href="/blog"
            className="text-[#EB5644] hover:underline font-semibold"
          >
            블로그로 돌아가기
          </Link>
        </div>
      </main>
    );
  }

  const IconComponent = post.icon;

  const handleCopyLink = () => {
    navigator.clipboard.writeText(window.location.href);
    setCopiedLink(true);
    setTimeout(() => setCopiedLink(false), 2000);
  };

  // 관련 포스트 가져오기
  const relatedPostsData = post.relatedPosts
    .map((relatedSlug) => {
      const relatedPost = blogPosts[relatedSlug as keyof typeof blogPosts];
      return relatedPost ? { slug: relatedSlug, ...relatedPost } : null;
    })
    .filter(Boolean);

  return (
    <main className="bg-gray-50">
      {/* Breadcrumb */}
      <div className="bg-white border-b">
        <div className="container mx-auto px-4 py-3">
          <div className="flex items-center gap-2 text-sm text-gray-600">
            <Link href="/" className="hover:text-[#EB5644]">
              홈
            </Link>
            <span>/</span>
            <Link href="/blog" className="hover:text-[#EB5644]">
              블로그
            </Link>
            <span>/</span>
            <span className="text-gray-900">{post.title}</span>
          </div>
        </div>
      </div>

      {/* Article Container */}
      <div className="container mx-auto px-4 py-12">
        <div className="max-w-4xl mx-auto">
          {/* Back Button */}
          <Link
            href="/blog"
            className="inline-flex items-center gap-2 text-gray-600 hover:text-[#EB5644] mb-6 transition-colors font-medium"
          >
            <FaArrowLeft />
            <span>블로그 목록으로</span>
          </Link>

          {/* Article Header */}
          <article className="bg-white rounded-2xl shadow-sm overflow-hidden">
            {/* Category & Meta */}
            <div className="p-8 pb-6">
              <div className="flex flex-wrap items-center gap-4 mb-4">
                <div
                  className="flex items-center gap-2 px-3 py-1.5 rounded-full text-sm font-semibold text-white"
                  style={{ backgroundColor: post.iconColor }}
                >
                  <IconComponent />
                  <span>{post.category}</span>
                </div>
                <div className="flex items-center gap-4 text-sm text-gray-500">
                  <div className="flex items-center gap-1.5">
                    <FaClock />
                    <span>{post.readTime}</span>
                  </div>
                  <div className="flex items-center gap-1.5">
                    <FaTag />
                    <span>{post.date}</span>
                  </div>
                </div>
              </div>

              {/* Title */}
              <h1 className="text-3xl md:text-4xl font-bold text-gray-900 mb-4 leading-tight">
                {post.title}
              </h1>

              {/* Excerpt */}
              <p className="text-lg text-gray-600 leading-relaxed">
                {post.excerpt}
              </p>
            </div>

            {/* Share Buttons */}
            <div className="px-8 pb-6 flex items-center gap-3">
              <span className="text-sm text-gray-600 font-medium">
                공유하기:
              </span>
              <div className="flex items-center gap-2">
                <button
                  onClick={handleCopyLink}
                  className="flex items-center justify-center w-9 h-9 rounded-lg bg-gray-100 hover:bg-gray-200 text-gray-700 transition-colors"
                  title="링크 복사"
                >
                  <FaLink className="text-sm" />
                </button>
                <a
                  href={`https://www.facebook.com/sharer/sharer.php?u=${
                    typeof window !== "undefined" ? window.location.href : ""
                  }`}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="flex items-center justify-center w-9 h-9 rounded-lg bg-blue-600 hover:bg-blue-700 text-white transition-colors"
                  title="Facebook 공유"
                >
                  <FaFacebookF className="text-sm" />
                </a>
                <a
                  href={`https://twitter.com/intent/tweet?url=${
                    typeof window !== "undefined" ? window.location.href : ""
                  }&text=${post.title}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="flex items-center justify-center w-9 h-9 rounded-lg bg-sky-500 hover:bg-sky-600 text-white transition-colors"
                  title="Twitter 공유"
                >
                  <FaTwitter className="text-sm" />
                </a>
              </div>
              {copiedLink && (
                <span className="text-sm text-[#EB5644] font-medium">
                  링크가 복사되었습니다!
                </span>
              )}
            </div>

            {/* Divider */}
            <div className="border-t border-gray-200"></div>

            {/* Content */}
            <div className="p-8 md:p-12">
              <div className="prose prose-lg max-w-none">
                {post.sections.map((section) => (
                  <div
                    key={section.id}
                    id={section.id}
                    className="mb-8 scroll-mt-20"
                  >
                    <h2 className="text-2xl md:text-3xl font-bold text-gray-900 mb-4 pb-2 border-b-2 border-[#EB5644]/20">
                      {section.title}
                    </h2>
                    {section.content.split("\n\n").map((paragraph, idx) => {
                      if (paragraph.trim().startsWith("•")) {
                        const items = paragraph
                          .split("\n")
                          .filter((line) => line.trim().startsWith("•"));
                        return (
                          <ul key={idx} className="space-y-2 my-4 ml-6">
                            {items.map((item, itemIdx) => {
                              const content = item.replace("•", "").trim();
                              return (
                                <li
                                  key={itemIdx}
                                  className="text-gray-700 leading-relaxed list-disc"
                                  dangerouslySetInnerHTML={{
                                    __html: content.replace(
                                      /\*\*(.*?)\*\*/g,
                                      "<strong>$1</strong>"
                                    ),
                                  }}
                                />
                              );
                            })}
                          </ul>
                        );
                      } else if (paragraph.trim().match(/^\d+\./)) {
                        const items = paragraph
                          .split("\n")
                          .filter((line) => line.trim().match(/^\d+\./));
                        return (
                          <ol
                            key={idx}
                            className="space-y-2 my-4 ml-6 list-decimal"
                          >
                            {items.map((item, itemIdx) => {
                              const content = item.replace(/^\d+\./, "").trim();
                              return (
                                <li
                                  key={itemIdx}
                                  className="text-gray-700 leading-relaxed"
                                  dangerouslySetInnerHTML={{
                                    __html: content.replace(
                                      /\*\*(.*?)\*\*/g,
                                      "<strong>$1</strong>"
                                    ),
                                  }}
                                />
                              );
                            })}
                          </ol>
                        );
                      } else if (
                        paragraph.trim().startsWith("**") &&
                        paragraph.trim().endsWith("**")
                      ) {
                        const text = paragraph.replace(/\*\*/g, "");
                        return (
                          <h3
                            key={idx}
                            className="text-xl font-bold text-gray-900 mt-6 mb-3"
                          >
                            {text}
                          </h3>
                        );
                      } else if (paragraph.trim()) {
                        const htmlContent = paragraph.replace(
                          /\*\*(.*?)\*\*/g,
                          "<strong>$1</strong>"
                        );
                        return (
                          <p
                            key={idx}
                            className="text-gray-700 leading-relaxed mb-4 text-base"
                            dangerouslySetInnerHTML={{ __html: htmlContent }}
                          />
                        );
                      }
                      return null;
                    })}
                  </div>
                ))}
              </div>
            </div>
          </article>

          {/* Related Posts */}
          {relatedPostsData.length > 0 && (
            <div className="mt-12">
              <h2 className="text-2xl font-bold text-gray-900 mb-6">관련 글</h2>
              <div className="grid md:grid-cols-2 gap-6">
                {relatedPostsData.map((relatedPost: any) => {
                  const RelatedIcon = relatedPost.icon;
                  return (
                    <Link
                      key={relatedPost.slug}
                      href={`/blog/${relatedPost.slug}`}
                      className="group bg-white rounded-xl shadow-sm hover:shadow-md transition-all overflow-hidden"
                    >
                      <div className="p-6">
                        <div
                          className="flex items-center gap-2 px-3 py-1.5 rounded-full text-sm font-semibold text-white mb-4 w-fit"
                          style={{ backgroundColor: relatedPost.iconColor }}
                        >
                          <RelatedIcon />
                          <span>{relatedPost.category}</span>
                        </div>
                        <h3 className="text-xl font-bold text-gray-900 mb-2 group-hover:text-[#EB5644] transition-colors">
                          {relatedPost.title}
                        </h3>
                        <p className="text-gray-600 text-sm line-clamp-2">
                          {relatedPost.excerpt}
                        </p>
                        <div className="flex items-center gap-3 mt-4 text-xs text-gray-500">
                          <span className="flex items-center gap-1">
                            <FaClock />
                            {relatedPost.readTime}
                          </span>
                          <span>{relatedPost.date}</span>
                        </div>
                      </div>
                    </Link>
                  );
                })}
              </div>
            </div>
          )}

          {/* CTA Section */}
          <div className="mt-12 bg-gradient-to-r from-[#EB5644] to-[#D84A3A] rounded-2xl p-8 md:p-12 text-center text-white shadow-lg">
            <h2 className="text-2xl md:text-3xl font-bold mb-4">
              SNS 성장을 시작하세요
            </h2>
            <p className="text-lg mb-8 text-white/90">
              전문가의 도움으로 더 빠르고 안전하게 계정을 성장시킬 수 있습니다
            </p>
            <div className="flex flex-col sm:flex-row gap-4 justify-center">
              <button
                onClick={() => {
                  if (typeof window !== "undefined" && (window as any).ht) {
                    (window as any).ht.open();
                  }
                }}
                className="inline-block px-8 py-3 rounded-lg font-semibold bg-white text-[#EB5644] hover:bg-gray-100 transition-colors shadow-md"
                style={{ cursor: "pointer" }}
              >
                무료 상담하기
              </button>
              <Link
                href="/pricing"
                className="inline-block px-8 py-3 rounded-lg font-semibold bg-white/10 hover:bg-white/20 border-2 border-white/30 transition-colors backdrop-blur-sm"
              >
                가격 안내 보기
              </Link>
            </div>
          </div>
        </div>
      </div>
    </main>
  );
}
